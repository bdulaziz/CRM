    public String forAdd(){
        String s = this.id.getText()+","+this.name.getText()+","+this.email.getText()+","+this.phone.getText()+","
                +this.lastContacted.getText()+","+this.status.getSelectedItem()+","+this.intrested.getSelectedItem()+","+"\n";
        return s;
    }
