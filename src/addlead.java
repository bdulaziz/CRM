/** 
 * Method to connect the submit button to the tableinfo frame   
 */
    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        Writer w;
        String file = "recources/userdet+ail.txt";

        try {
            w = new BufferedWriter(new FileWriter(file, true));
            w.append(this.forAdd());
            w.close();
        } catch (IOException ex) {
            Logger.getLogger(addlead.class.getName()).log(Level.SEVERE, null, ex);
        }
        tableinfo ti = new tableinfo();
        ti.setVisible(true);
        this.setVisible(false);
    }//GEN-LAST:event_submitActionPerformed
